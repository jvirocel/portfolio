        // Theme toggle functionality
        const themeToggle = document.querySelector('.theme-toggle');
        const body = document.body;
        const toggleThumb = themeToggle.querySelector('.toggle-thumb');
        const sunIcon = themeToggle.querySelector('.fa-sun');
        const moonIcon = themeToggle.querySelector('.fa-moon');

        // Check for saved theme preference
        const currentTheme = localStorage.getItem('theme');
        if (currentTheme === 'dark') {
            body.classList.add('dark-mode');
            toggleThumb.style.transform = 'translateX(30px)';
            sunIcon.style.opacity = '1';
            moonIcon.style.opacity = '0';
        } else {
            toggleThumb.style.transform = 'translateX(0)';
            sunIcon.style.opacity = '0';
            moonIcon.style.opacity = '1';
        }

        // Toggle theme
        themeToggle.addEventListener('click', () => {
            body.classList.toggle('dark-mode');
            const isDarkMode = body.classList.contains('dark-mode');
            
            // Animate toggle thumb
            toggleThumb.style.transform = isDarkMode ? 'translateX(30px)' : 'translateX(0)';
            
            // Toggle icons
            sunIcon.style.opacity = isDarkMode ? '1' : '0';
            moonIcon.style.opacity = isDarkMode ? '0' : '1';
            
            // Save preference
            localStorage.setItem('theme', isDarkMode ? 'dark' : 'light');
        });
    </script>
